    # تجميع سجلات ملخص الأخطاء العام للمجلد الأول
    error_summary_records.append({
        "Model_Name": model_key,
        "Total_Test_Samples": total_samples,
        "Total_Misclassified_Errors": num_errors,
        "Error_Rate_Percentage": round(error_percentage, 2)
    })

    # بناء الجيسون العام للمجلد الأول
    global_summary_metrics[model_key] = {
        "Accuracy": float(summary["acc"]),
        "Precision_Macro": float(summary["prec"]),
        "Recall_Macro": float(summary["rec"]),
        "F1-Score_Macro": float(summary["f1"]),
        "Total_Errors": num_errors,
        "Global_Best_Epoch": int(summary["epoch_mode"]),
        "Cross_Fold_F1_Scores": [float(v) for v in summary["fold_f1s"]],
        "F1_StdDev": float(np.std(summary["fold_f1s"]))
    }

    # 🎨 حفظ مصفوفة الارتباك (المجلد الثاني)
    cm = confusion_matrix(all_labels, all_preds)
    plt.figure(figsize=(8, 6))
    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues", xticklabels=correct_class_names, yticklabels=correct_class_names, cbar=False)
    plt.title(f"Confusion Matrix: {model_key}", fontsize=12, fontweight='bold', pad=10)
    plt.xlabel("Predicted Labels", fontweight='bold')
    plt.ylabel("True Labels", fontweight='bold')
    plt.tight_layout()

    cm_path = os.path.join(dir_matrices, f"{model_key}_confusion_matrix.png")
    plt.savefig(cm_path, dpi=300)
    plt.close()
    print(f"   ✔ Routed: Confusion Matrix image -> [2_Model_Confusion_Matrices/]")

    # 📄 حفظ سجل العينات النصية المخطئة بالكامل (المجلد الثالث)
    if misclassified_records:
        df_errors = pd.DataFrame(misclassified_records)
        errors_csv_path = os.path.join(dir_errors, f"{model_key}_misclassified_examples.csv")
        df_errors.to_csv(errors_csv_path, index=False, encoding="utf-8-sig")
        print(f"   ✔ Routed: Individual errors CSV -> [3_Model_Misclassifications/]")

        # ➔ ➔ ➔ حساب وحفظ تكرارات الأخطاء المحددة (المجلد الرابع المطلوب)
        pairs_counts = {}
        for rec in misclassified_records:
            pair_key = (rec["True_Class"], rec["Predicted_Class"])
            pairs_counts[pair_key] = pairs_counts.get(pair_key, 0) + 1

        sorted_pairs = sorted(pairs_counts.items(), key=lambda x: x[1], reverse=True)

        # 1. حفظها كـ CSV منسق للفريق
        freq_records = []
        for rank, ((true_c, pred_c), count) in enumerate(sorted_pairs, 1):
            freq_records.append({
                "Rank": rank,
                "Ground_Truth_Class": true_c,
                "Misclassified_As": pred_c,
                "Total_Occurrences": count
            })
        df_freq = pd.DataFrame(freq_records)
        freq_csv_path = os.path.join(dir_frequencies, f"{model_key}_error_frequencies.csv")
        df_freq.to_csv(freq_csv_path, index=False, encoding="utf-8-sig")

        # 2. حفظها كتقرير نصي أنيق يشبه طباعة الكود تماماً (.txt) لسهولة القراءة السريعة
        freq_text_path = os.path.join(dir_frequencies, f"{model_key}_error_frequencies_report.txt")
        with open(freq_text_path, "w", encoding="utf-8") as f_txt:
            f_txt.write(f"===========================================================================\n")
            f_txt.write(f"📊 DETAILED MISCLASSIFICATION FREQUENCIES FOR MODEL: {model_key.upper()}\n")
            f_txt.write(f"===========================================================================\n")
            f_txt.write(f"{'No.':<4} | {'Ground Truth Class':<25} ➔ {'Misclassified As':<25} | {'Count':<5}\n")
            f_txt.write("-" * 75 + "\n")
            for rank, ((true_c, pred_c), count) in enumerate(sorted_pairs, 1):
                f_txt.write(f"{rank:<4} | {true_c:<25} ➔ {pred_c:<25} | {count:<5} times\n")
            f_txt.write(f"===========================================================================\n")
        print(f"   ✔ Routed: Detailed transition frequencies (CSV & TXT) -> [4_Detailed_Error_Frequencies/]")
    else:
        print("   🎉 Exceptional performance! No errors found to process for this model.")

