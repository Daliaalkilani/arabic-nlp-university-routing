# التحقق من توفر الـ GPU لضمان تسريع عملية تدريب النموذج
print("CUDA available:", torch.cuda.is_available())

# تحديد الجهاز المستخدم (إن وجد GPU يتم اعتماده، وإلا يتم العودة للـ CPU)
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

